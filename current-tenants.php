        <i class="fas fa-chevron-circle-up"></i> WHEN ARE LATE FEES CHARGED?
        <div class="tab-info">
        Typically, with leases that we write, rent is "late" on the 3rd of the month. It doesn't matter if the 1st, 2nd or 3rd all fall on a weekend or holiday days. Tenants have the option and obligation to get their payment in BEFORE the 1st of the month. There is nothing to prevent anyone from paying on the 26th, 27th or 28th prior to the 1st when the rent is due. That is the easiest and best way to avoid late charges.
