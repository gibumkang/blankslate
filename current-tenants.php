    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> HOW DOES AN EVICTION WORK AFTER THE 5 DAY HAS EXPIRED?
        <div class="tab-info">
        The day after the 5 day expires, we fill out the necessary court forms and file with both the Justice Court and the Constable's office. From there the paperwork, including a copy of the 5 day Notice goes in front of the judge. This process can take 1-3 days depending on the courts work load. The judge will sign the Order for eviction and send it to the Constable's office. At that point, the Constable will normally post a 24 hour notice, again this could take a day or two depending on their work load. This notice is not required by the Constables office. They do it as a courtesy to you the tenant and of course they are like anybody else and would like to avoid as many problems as possible. The next day, assuming they did post a 24 hour notice, they will be back with a locksmith and will remove anyone and anything that is breathing. That includes pets. Your pets will go to the animal shelter if you are not there to take them with you. The same thing could happen to unattended children (minors) as they would end up in the Juvenile care system. The locksmith will change all the locks and you will not be allowed back into the property.
        </div>
