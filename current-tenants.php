    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> IF YOU START AN EVICTION, AND I HAVE MONEY, DON'T YOU HAVE TO TAKE IT?
        <div class="tab-info">
        It depends on whether or not you have <strong>ALL OF THE MONEY.</strong> Once an eviction is started, other eviction charges and fees start to add up quickly. We are not "required" to take money unless it is for the full amount. And then it needs to be either cashiers check or money order... no personal or business checks. We can also "hold" your partial funds as additional Deposit until you get all of the money. <strong>In this case we ARE NOT accepting rent.</strong> The eviction process will continue and a game of "Beat The Constable" will begin. If you are successful, you will pay off your account in full and we will stop the eviction process before the Constable is at your door. It's entirely up to you.
