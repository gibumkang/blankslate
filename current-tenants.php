    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> WHAT IS CONSIDERED PROPER NOTICE WHEN VACATING?
        <div class="tab-info">
        You need to give written notice at least 30 days in advance of your departure, and it must be on or just before the 1st of the month when the notice is given or presented to the office or your property manager.
        </div>
