    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> WHEN DO YOU HAVE TO ACCEPT LATE RENT?
        <div class="tab-info">
        As long as it is before the end of the 5 day notice to Pay Or Quit, you are paying the entire balance owed, and your funds are in the form of a money order or cashiers check.
        </div>
