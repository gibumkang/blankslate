    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> WHAT HAPPENS IF THE REPAIRS, CLEANING, AND OUTSTANDING CHARGES TOTAL MORE THAN THE DEPOSIT I HAVE?
        <div class="tab-info">
        That in part is up to the landlord. We usually turn over ledgers with outstanding balances to Collection Agency's. They in turn see to it that it gets on your credit bureau. Some owners are a little more aggressive and they start a Small Claims suit. It's not an enjoyable experience for anyone. Keep in mind, that everything is Tenant generated. In other words, if you did your part, had everything paid for, there wouldn't be any Collection Agency's or court actions involved.
        </div>
    </div>

    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> CAN I REPLACE SOME OF THE CEILING LIGHTS WITH CEILING FANS/LIGHTS?
        <div class="tab-info">
        Maybe. You need to check with your property manager. Keep in mind, that according to state law, anything that is attached to the property, like a ceiling fan, becomes part of the real estate and is no longer your personal property. It is part of the owners improved real estate. Technically, if you remove the ceiling fans when you vacate, you are stealing the owners property. It is best to get an agreement signed that allows you to remove the attached items before you install them. That way everybody is on the same page and there aren't any problems.
        </div>
    </div>

    <div class="accordion-tab">
