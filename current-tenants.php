    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> CAN I BE EVICTED IF I DON'T PAY FOR REPAIRS?
        <div class="tab-info">
        Yes, if you are responsible for the repairs. Let's say as an example that you didn't take care of the lawn and we had to send someone out to maintain it. Those expenses would get charged to your ledger, according to your lease agreement. Then when you made a payment, those charges would be paid first. Assuming that you only paid an amount equal to your regular rent, your account will be short and "rent" will be owning. Most lease agreements also stipulate that ANY unpaid charge becomes "rent" due after 30 days... that includes any unpaid late fees. Sooo, the bottom line is you can basically be evicted over anything that remains unpaid on your ledger.
        </div>
    </div>

    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> DO YOU REALLY RETURN SECURITY DEPOSITS?
        <div class="tab-info">
        Yes, all the time. However, you have to leave the property in excellent condition. It has to be ready so that someone else could move right in. Most tenants don't want to go to that much trouble to get a place in shape. They ignore the little things like cleaning windows and blinds. Or they will attempt to clean the carpet themselves with less than adequate results. However, for those tenants that do what is necessary, we return their deposits.
        </div>
    </div>

    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> WHAT DO I NEED TO DO TO GET MY SECURITY DEPOSIT BACK?
        <div class="tab-info">
        Well, first of all, you will need to have all outstanding charges on your ledger paid. Secondly, you will need to give proper written notice that you will be vacating. Finally, the property has to be in ready to move-in condition.
