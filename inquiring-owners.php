        <i class="fas fa-chevron-circle-up"></i> WHAT ARE YOUR FEELINGS ABOUT PETS AND SMOKERS?
        <div class="tab-info">
        They exist and make up large paying demographic groups. Let's take pets. Tenants know that only a <strong>small percentage</strong> of owners 'allow' pets. Consequently, many pet owners feel they must deceive a landlord about having a pet. They justify it in their mind because they don't see their pet as being a problem. This means that on occasion you will rent to a person without pets only to find later on that they have a pet. At this point you have a decision to make. Remember, the property is an investment. The question you need to ask yourself is..if the pet has caused damage, and you evict the tenant because of the pet, will you be better off? You will get as much of the deposit as possible to make the repairs and you will have a vacancy.... no income. If you let the pet stay <strong>(assuming no more damages),</strong> you will still have to make the repairs when the tenant decides to leave, but you will have the income while the tenant is in the property. If the pet isn't causing any harm, it's always better to let them stay. The same goes for smokers. We have had properties where all the carpet had to be replaced, and the unit painted twice, just to get rid of the smoke stains and odors. The choice, from a risk standpoint would be to avoid all smokers and pet owners. The reality is, they're not always going to tell you the truth and you will end up with them anyway. So, maybe it would be better to allow both groups but put up restrictions. It's not an easy question to answer. For the most part it depends on you. It's your property. The more people you exclude, potentially the longer it will take to rent.... and on average the vacancy will always cost more than making the repairs.
