        <div class="tab-info">
            <h3>Giving Notice</h3>
            <p>
            Most Rental Agreements, including ours, requires that a 30 day written notice be given when a tenant has decided to move. How exactly though does this work? Thirty days starting when? What other requirements are there of the notice?
            </p>
            <p>
                Many tenants get confused as to when they can move out without losing their deposit because of improper notice. Here again, giving the notice has nothing to do when you actually "physically" move. It has to do with the amount of rent paid and basically giving the owner notice that the rental stream is going to stop. You could have physically moved out months ago, and just kept paying rent for your own personal reasons... and then given 30 days notice in the last month of your agreement. The 30 day notice has more to do with money than with when you actually physically move. Having said that, most tenants give notice when they are ready to actually move.
            </p>
            <p>
            So there are two requirements. You must give a minimum of 30 days written notice, and it must be given on or before the first of the month. That means if you are moving out on the 15th of June, then your notice would have to be given on the 1st of May... which is 45 days early. Remember at least 30 days, and it must be on the 1st of any calendar month.
            </p>
            <h3>Middle of the Month</h3>
            <p>When you move in the middle of the month, with proper notice, you are still required to pay in advance for the days you anticipate on being in the property. We will still continue to start the eviction process, since you have not paid a full months rent. Then assuming you leave on time, the eviction will be terminated. Otherwise, you will be required to pay the balance of the rent owed, or the eviction will go forward.</p>
            <h3>Stopping the Rental Clock</h3>
            <p>You are charged for rent up until the time ALL the keys and remotes are returned... whether you moved out before returning these items or not. As long as you have possession of keys or a way to enter the property, then you are still in possession of the unit.</p>
            <h3>Final Inspection - Walk-thru</h3>
            <p>
            The final inspection will not be completed until you have completely moved out and either have or are returning the keys at the time of the inspection. It's impossible to inspect with furniture in the residence, and most often damage will occur during the move-out process.
            </p>
