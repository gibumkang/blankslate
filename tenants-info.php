        <h3>When Rent Is Due vs Being Late</h3>
        <p>
        Tenants, somehow get confused between when rent is due and when rent is late. The time frame in which late fees can be applied, has absolutely nothing to do with when rent is due, or when the eviction process can be started. They are entirely three different and distinctly separate timelines.
        </p>
        <p>
        <strong>Rent is due on the 1st of each month...</strong>period! That means that an eviction process can be started on the 2nd of the month, regardless of when a payment may be considered "late" for charging late fees or any other penalty. So as an example, if a lease happened to stipulate that late fees wouldn't be charged, or rent was to be considered late until after the 15th of the month, there is the distinct possibility that a landlord could start the eviction process on the 2nd of the month and have a lock-out completed before the payment would be "late"... again, late in that fees could be charged.
        </p>
        <h3>Are Late Fees, Rent?</h3>
        <p>
        Most Rental Agreements stipulate that any outstanding charges, including late fees, become rent after they have gone unpaid for 30 days. So, yes, not paying your late fees or other charges means that you can be evicted for that as well since those amounts become rent. In our office, and with our agreements, we go a step further and stipulate that ALL outstanding charges, whether for late fees, utilities, or whatever, will be paid FIRST, before any funds are applied to rent owed.
        </p>
        <h3>Money Orders vs Personal Checks</h3>
        <p>
        Many tenants do not realize that personal checks (actually ANY type of check, including bank checks) are not legal tender and do not have to be accepted for payment. However, as a practical business matter, we do accept checks for the convenience of our tenants. We do not accept personal checks at the time of move-in, nor do we continue to accept checks with established tenants once a tenant has bounced one. A tenants account will be marked not to accept personal checks and should one be mailed in, it will be returned, unused. Also, if your payment is late, we will not accept a personal check. The above applies for electronic ACH payments as well.
        </p>
        <h3>Bad Luck and Excuses</h3>
        <p>After 30 years we've heard everything. Everyone has problems, all the time. It is your responsibility, no matter what the circumstance, to get your payments in when they are due. With the availability of Electronic Banking and Electronic Payments 24/7 from anywhere in the world, there really aren't any valid excuses. If you mail in a check, it is NOT the Post Offices, or the mailman's job to get your rent to us in a specific amount of time. If you are going to rely on the post man then you need to be ready to accept the fact that sometimes your payment is not going to get here on time and you are going to incur late fees. We would suggest that you use the electronic ACH payments that is free, or mail your rent at least two (2) weeks in advance in order to minimize any chance of slow mail.</p>
