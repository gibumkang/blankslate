        <h3>Getting deposits back:</h3>
        <p>Most tenants are concerned with their ability to get their Security Deposits returned to them. First of all, we like to give all of the deposit back and we frequently return all or part of a tenants security deposit. However there are several things that you need to do.</p>
        <p>First of all, you will need to have all outstanding charges on your ledger paid. Secondly, you will need to give proper written notice that you will be vacating. Finally, the property has to be in ready to move-in condition. Most tenants don't want to go to that much trouble to get a place in shape. They ignore the little things like cleaning windows and blinds. Or they will attempt to clean the carpet themselves with less than adequate results. However, for those tenants that do what is necessary, we return their deposits.</p>
        <h3>Owner holding deposits:</h3>
        <p>It's much more difficult for us to return deposits if we never got them. Many times we'll get involved with a property after a tenant has moved in and they had given the owner their Security Deposit. In this instance the tenant will need to get in touch with the owner for the return of their deposit.. remember, we never had it.</p>
        <h3>Deposit returned - time frames:</h3>
        <p>It can take from a few days to as long as thirty (30) days to get your deposit returned to you. For the most part it depends on you. If the unit is rent ready, ready to go, it usually takes a few days. If there are repairs, and cleaning that need to be done, then probably it will be closer to 30 days. The more you do, then the less we need to do, and the faster a return can usually be made.</p>
        <p>You need to make sure that we have all of your forwarding information on file when you vacate. It's difficult to return deposits if we don't know where you've gone to.</p>
        <p><strong>Property Problems - Evictions</strong> If there has been an eviction, of if there is a lot of work to be done, it isn't likely that any portion of the deposit will be eligible to be returned. In fact, if there still remains unpaid charges for rent, court fees, or cleaning and repairs, it's possible that not only will all of a tenants deposit be used up, but that the remaining balance owed would be turned over to a collection agency or possibly small claims court.</p>
