        <i class="fas fa-chevron-circle-up"></i> Maintenance
        <div class="tab-info">
        <p>
        The basic rule of thumb that is followed is "If you break it, clog it, or don't maintain it, then it's something you need to fix". If it breaks on it's own then the owner takes care of it. An example of what the owner would handle would be if the water heater started leaking, or the a/c stopped working because the compressor needed replacing. In other words, items that the tenant couldn't possibly be involved with... like repairing a leaky roof.
        </p>
        <p>
        Also, keep in mind that during the summer especially, and at times during the winter, when your heating or air conditioning is out, so is everyone else's. It is not unusual for a/c repair men to be booked solid a week in advance. There might be a delay in making the repair.
        </p>
        <p>
        Everything else is pretty much up to the tenant to keep working. Below are some common problems and their remedies. Remember, if you call for a repair and there really wasn't anything wrong, then you will be paying for the service call. You will also be paying for the service call if you are not present at the appointed time to let a tech fix the problem.
        </p>
        <p>
        <strong>Electrical Light or Outlet(s) not working.</strong> Usually this is caused by a breaker switch having been overloaded and tripped off. There are two areas to check. First the breaker box panel which is normally on the outside of the building, but could also be in a garage or tucked away in the back of a closet. First look to see if any of the switches have slightly moved away from the totally "on" position. If you see one that is, take and switch it completely to the "off" position before attempting to move it to the "on" position. Assuming that the breaker holds, this procedure should fix the problem.
        </p>
        <p>
        A second type of breaker is found in areas of the home where there is water, usually in the kitchen or bathroom, but also in the garage. They are GFI breaker plugs and they control a wider area of plugs than just the spot they are located at. Consequently you could have a plug or plugs go out in a bathroom and it is a GFI breaker plug in a garage that has tripped. Find the culprit plug and push the "reset" button which is located in the middle of the plug. That should reset the power to the problem area.
        </p>
        <p>
            <strong>Garbage Disposal</strong> - Your garbage disposal has gotten jammed, or is making a humming noise but not grinding. Usually something has gotten lodged along the inside wall of the disposal that is preventing them from working. On most models there is a "Reset" button on the bottom of the disposal. Make sure that it is pushed in and try the disposal again. If it didn't help the next step is to manually dislodge the interior grinding mechanism. This is done by inserting an allen wrench into the bottom of the disposal and twisting/cranking the wrench to get the mechanism freed up.
        </p>
        <p>
            <strong>Leaking Pipes/Water Heater</strong> - The first thing to do is get the water shut off. Every sink, toilet, water heater has a shut off valve next to the wall where the water is entering the apparatus from. TURN THE WATER OFF. Get towels or a mop and get the water soaked up. Do not let the water ruin carpet, walls, flooring etc. Call us immediately so we can get someone out to fix, repair, or complete clean-up.
        </p>
        <p>
            <strong>Clogs</strong> - If the clog is caused by normal use, then you need to call a plumber and deal with it. On rare occasion there might be old tree roots that have infiltrated the plumbing lines. In that case, it would be the owners responsibility to keep the pipes open.
        </p>
        <p>
            <strong>Dishwashers</strong> - Many people are not in the habit of using a dishwasher... they like to do dishes by hand. That is well and good except that dishwashers have seals that can dry out and crack if the dishwashers are not used. Consequently, if you prefer to do dishes by hand, then you will need to run a cycle through the dishwasher once a week to keep the seals from leaking. Otherwise, when they dry out, you'll have a repair bill to deal with.
