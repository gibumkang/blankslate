        <div class="tab-info">
        <h3>Laws favor the owners:</h3>
        <p>
        Most tenants in Clark County are transplants from another state. They do not realize that State laws here, especially regarding tenant evictions, can be so radically different. Consequently, they are almost dumbfounded to find that an eviction has actually happened, to them.
        </p>
        <p>
        Because of the high transient nature of Clark County residents, the laws are written in favor of the property owner or landlord. The states forefathers quickly learned that everyone and his brother would soon come to Nevada to 'win' their fortunes in life at the tables. They needed a way to protect their investments from the hopeful masses. Consequently, they made it very easy to evict people whether it be out of casinos, hotels, or any other type of real estate.
        </p>
        <h3>Eviction time frames:</h3>
        <p>Evictions for non payment of rent typically takes 21 days, start to finish. Compare that with up to 3-6 months as an average in many other states. The process is straight forward.</p>
        <h3>Everyone has problems:</h3>
        <p>Tenants will normally find that property management firms in general are very unsympathetic to their situation... we have to be, otherwise we would go nuts taking on everyone else's problems. Everyone has problems in life, all the time. At this point, you have become a problem for us and the owner.</p>
        <h3>Avoiding an eviction:</h3>
        <p>The best way to avoid an eviction is to get money from anyone that doesn't have the power to evict you. That might include friends, relatives, social services, advances from work, or your church. Another surprise many tenants have is that the evictions actually do take place no matter what their circumstances may be.</p>
