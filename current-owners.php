        <div class="tab-info">
        In addition to all of the possible reasons given above, the most common reason is because the tenant moved in, in the <strong>middle</strong> of the prior month. When this happens, we make the tenant pay for a full month of rent at move-in, and then pay the smaller, prorated portion the second month. Doing it this way prevents a tenant of say moving in on the 23rd and only paying one week in rent, and then not paying again and we immediately have to start an eviction. So usually what happens is that they pay the full months rent at move-in, from which Referral Fees, Tenant Placement Fees, Owner Reserve etc is subtracted from, then the second month the tenant only owes for a couple of weeks or so, and the check is again less than what an owner will normally expect to receive.
