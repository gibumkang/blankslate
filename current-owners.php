    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> WILL I GET THE TENANTS WHOLE DEPOSIT IF THERE ARE REPAIRS NEEDED?
        <div class="tab-info">
        Sometimes the hardest thing to get owners to understand is that their personal living style can not be mandated unto a tenant. So, if you would replace a mini blind simply because one of the fingers going across it was bent, doesn't mean that would be imposed on a tenant as a deduction from their deposit. The courts look at it more from a reasonable wear and tear standpoint and what might be considered average living conditions. We've had owners replace all the carpet in a unit because of some small stains in one back bedroom and expect the tenants deposit to foot the bill. It doesn't work that way. You can only charge a tenant for damage, and other work that needs to be done beyond what would rationally be considered normal wear and tear.
        </div>
    </div>

    <div class="accordion-tab">
        <i class="fas fa-chevron-circle-up"></i> WHAT HAPPENS IF THE TENANT HASN'T PAID AFTER THE 5 DAY PAY OR QUIT IS UP?
        <div class="tab-info">
        At that point we file with the appropriate Court for an Eviction. Depending which city the property is located in, it can take from one to three business days to process. After that, the court forwards the signed order to the Constable's office. The Constable then posts a 24 hour Notice to Vacate. That posting can take one to two days. From there, the Constable goes out the following day, with a locksmith, and physically removes everyone in the property. After that it usually takes a day or two to get the new keys from the locksmith.
        </div>
